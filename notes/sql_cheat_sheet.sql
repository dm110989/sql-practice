-- 🔢 Числа
ABS(x)         -- модуль числа → ABS(-5) = 5
ROUND(x, n)    -- округление → ROUND(10.567, 2) = 10.57
CEIL(x)        -- округление вверх → CEIL(10.1) = 11
FLOOR(x)       -- округление вниз → FLOOR(10.9) = 10

-- -----------------------------------------

-- 🔤 Строки
LOWER(text)        		-- нижний регистр → 'ABC' → 'abc'
UPPER(text)        		-- верхний регистр → 'abc' → 'ABC'
LENGTH(text)       		-- длина строки
SUBSTRING(text, a, b) 	-- часть строки (с позиции a, длина b)
CONCAT(a, b)       		-- склеить строки

-- -----------------------------------------

-- 📅 Даты и время
NOW()                 -- текущая дата и время

DATE_PART('day', interval)
-- достаёт часть из интервала
-- можно использовать: 'day', 'month', 'year', 'hour'

DATE_TRUNC('month', date)
-- обрезает дату до уровня
-- 'month' → начало месяца
-- 'year' → начало года

-- пример:
-- DATE_PART('day', date1 - date2)
-- вернёт количество дней из интервала

-- -----------------------------------------

-- 🧠 Логика
CASE 
  WHEN условие THEN значение
  ELSE значение
END

-- пример:
-- CASE 
--   WHEN price > 1000 THEN 'дорого'
--   ELSE 'дёшево'
-- END

-- -----------------------------------------

-- 🧩 Работа с NULL
COALESCE(a, b)
-- если a = NULL → берёт b

-- пример:
-- COALESCE(city, zip_code)

