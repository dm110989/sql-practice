-- 📊 Агрегации
COUNT(*)     -- количество строк
SUM(x)       -- сумма
AVG(x)       -- среднее
MIN(x)       -- минимум
MAX(x)       -- максимум

-- -----------------------------------------

-- 🔗 JOIN
INNER JOIN   -- только совпадающие строки
LEFT JOIN    -- всё из левой таблицы + совпадения

-- -----------------------------------------

-- 📈 Сортировка
ORDER BY column ASC   -- по возрастанию
ORDER BY column DESC  -- по убыванию

