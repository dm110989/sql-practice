-- =========================================
-- 1. ЧИСЛОВЫЕ ФУНКЦИИ
-- =========================================

ABS(x)          -- модуль числа
-- ABS(-5) = 5

ROUND(x, n)     -- округлить до n знаков после запятой
-- ROUND(10.567, 2) = 10.57

CEIL(x)         -- округлить вверх до целого
-- CEIL(10.1) = 11

FLOOR(x)        -- округлить вниз до целого
-- FLOOR(10.9) = 10


-- =========================================
-- 2. СТРОКОВЫЕ ФУНКЦИИ
-- =========================================

LOWER(text)             -- нижний регистр
-- LOWER('ABC') = 'abc'

UPPER(text)             -- верхний регистр
-- UPPER('abc') = 'ABC'

LENGTH(text)            -- длина строки в символах
-- LENGTH('hello') = 5

SUBSTRING(text, a, b)   -- взять часть строки
-- a = с какой позиции (отсчёт с 1)
-- b = сколько символов взять
-- SUBSTRING('abcdef', 1, 3) = 'abc'

CONCAT(a, b)            -- склеить строки
-- CONCAT('sql', '123') = 'sql123'

TRIM(text)              -- убрать пробелы по краям
-- TRIM('  hello  ') = 'hello'


-- =========================================
-- 3. ПРИВЕДЕНИЕ ТИПОВ
-- =========================================

-- PostgreSQL-синтаксис
::type
-- пример:
SELECT
  customer_zip_code::varchar
FROM customers;

-- стандартный SQL-синтаксис (работает везде)
CAST(x AS type)
-- пример:
SELECT
  CAST(price AS numeric)
FROM orders;


-- =========================================
-- 4. ТЕКУЩАЯ ДАТА И ВРЕМЯ
-- =========================================

NOW()               -- текущая дата и время (с временной зоной)
-- NOW() = 2026-04-07 15:30:00

CURRENT_DATE        -- текущая дата без времени
-- CURRENT_DATE = 2026-04-07


-- =========================================
-- 5. DATE_PART → ДОСТАТЬ ЧАСТЬ ДАТЫ/ВРЕМЕНИ
-- =========================================

-- возвращает числовое значение нужной части даты

DATE_PART('year', created_at)       -- год
-- 2024-05-10 → 2024

DATE_PART('month', created_at)      -- месяц
-- 2024-05-10 → 5

DATE_PART('day', created_at)        -- день месяца
-- 2024-05-10 → 10

DATE_PART('hour', created_at)       -- час
-- 2024-05-10 12:30:00 → 12

DATE_PART('minute', created_at)     -- минуты
-- 2024-05-10 12:30:00 → 30

DATE_PART('second', created_at)     -- секунды (могут быть с дробной частью)
-- 2024-05-10 12:30:45 → 45

-- день недели: 0 = воскресенье, 6 = суббота
DATE_PART('dow', created_at)
-- 2024-05-12 (воскресенье) → 0

-- день недели ISO: 1 = понедельник, 7 = воскресенье
DATE_PART('isodow', created_at)
-- 2024-05-12 (воскресенье) → 7

DATE_PART('doy', created_at)        -- день года (1–365/366)
-- 2024-05-10 → 131

DATE_PART('week', created_at)       -- номер недели в году (ISO)
-- 2024-05-10 → 19

DATE_PART('quarter', created_at)    -- квартал (1–4)
-- 2024-05-10 → 2

-- эпоха: количество секунд с 1970-01-01 00:00:00 UTC
DATE_PART('epoch', created_at)
-- 1970-01-01 00:01:00 → 60


-- =========================================
-- 6. DATE_TRUNC → ОБРЕЗАТЬ ДАТУ ДО НУЖНОЙ ТОЧНОСТИ
-- =========================================

-- возвращает дату, обнулив всё ниже указанной единицы

DATE_TRUNC('year', created_at)      -- 2024-01-01 00:00:00
DATE_TRUNC('quarter', created_at)   -- 2024-04-01 00:00:00
DATE_TRUNC('month', created_at)     -- 2024-05-01 00:00:00
DATE_TRUNC('week', created_at)      -- начало недели (понедельник)
DATE_TRUNC('day', created_at)       -- 2024-05-10 00:00:00
DATE_TRUNC('hour', created_at)      -- 2024-05-10 12:00:00
DATE_TRUNC('minute', created_at)    -- 2024-05-10 12:34:00
DATE_TRUNC('second', created_at)    -- 2024-05-10 12:34:56

-- пример:
-- 2024-05-10 12:34:56
-- DATE_TRUNC('month', ...) = 2024-05-01 00:00:00

-- группировка по дням
SELECT
  DATE_TRUNC('day', created_at) AS day_start,
  COUNT(*) AS orders_cnt
FROM orders
GROUP BY day_start
ORDER BY day_start;

-- группировка по месяцам
SELECT
  DATE_TRUNC('month', created_at) AS month_start,
  COUNT(*) AS orders_cnt
FROM orders
GROUP BY month_start
ORDER BY month_start;
