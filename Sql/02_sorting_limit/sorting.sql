-- =========================================
-- Задача 2: 
-- Напишите SQL-запрос, который выведет все столбцы и значения в таблице products.
-- Тема: ORDER BY
-- =========================================

-- Условие: Напишите SQL-запрос, который выводит все столбцы таблицы products, 
        --  сортируя результат по возрастанию product_id и по убыванию поля product_brand.

SELECT
  *
FROM
  products
ORDER BY 
product_id,
product_brand DESC

