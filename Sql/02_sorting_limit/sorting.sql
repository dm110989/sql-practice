-- Тема: ORDER BY
-- =========================================

-- Условие: Поля в результирующей таблице: product_id, product_weight_g, product_category_name.

SELECT
product_id,
product_weight_g,
product_category_name
FROM 
Products
ORDER BY product_weight_g DESC
LIMIT 10

-- =========================================
-- Задача 11: 
-- Напишите SQL-запрос, который выводит ID продукта, 
-- название бренда в нижнем регистре — product_brand_lower, 
-- количество символов в названии бренда — length_brand. 
-- Ограничьте результат запроса 10 строками, отсортировав результат по количеству символов в названии бренда по возрастанию.
-- =========================================
-- Тема: LOWER
-- Условие: Поля в результирующей таблице: product_id, product_brand_lower, length_brand.
SELECT 
product_id,
LOWER(product_brand) AS product_brand_lower,
LENGTH(product_brand) AS length_brand

FROM 
products
ORDER BY LENGTH(product_brand)
LIMIT 10

