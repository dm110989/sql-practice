-- Задача 13: 
-- А вот и прилетел первый самый настоящий ad hoc. 
-- Менеджеры хотят посчитать, сколько бы стоили товары после применения скидки в 5% или промокода в 100 рублей.
-- Напишите SQL-запрос, который выведет ID продукта, текущую цену продукта, цену продукта со снижением на 5%, 
-- а также цену продукта со снижением на 100 рублей. Округлите цены со скидкой вниз. 
-- Отсортируйте результат запроса по убыванию ID продукта и по убыванию цены продукта. Используйте оператор AS для названия столбцов.
-- =========================================
-- Тема: FLOOR
-- Условие: Поля в результирующей таблице: product_id, price, price_5_perc_discount, price_100_rub_discount.
SELECT
product_id,
price,
FLOOR(price - (price * 0.05)) AS price_5_perc_discount,
FLOOR(price - 100) AS price_100_rub_discount
FROM order_items
ORDER BY product_id DESC, price DESC